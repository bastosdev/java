package BASIC;

public class ifElse {

    public static void main(String[] args) {

        int nota = 7;

        if (nota >= 10){
            System.out.println("Aprovado!");
        } else if (nota <= 7){
            System.out.println("Recuperação!");
        } else {
            System.out.println("Reprovado!");
        }

    }
}
