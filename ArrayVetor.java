public class ArrayVetor {

    public static void main(String[] args) {

        double[] numbers = new double[4];
        numbers[0] = 31.3;
        numbers[1] = 32.5;
        numbers[2] = 33.7;
        numbers[3] = 34.2;

        for (int print = 0; print < numbers.length; print++){
            System.out.println(numbers[print]);
        }


    }

}
