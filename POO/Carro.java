package POO;

public class Carro {

    private String branding;
    private String model;
    private int places;
    private double capFuel;
    private double conFuel;

    public String getBranding(){
        return this.branding;
    }
    public void setBranding(String branding){
        this.branding = branding;
    }
    public String getModel(){
        return this.model;
    }
    public void setModel(String model){
        this.model = model;
    }
    public int getPlaces(){
        return this.places;
    }
    public void setPlaces(int places){
        this.places = places;
    }
    public double getCapFuel(){
        return this.capFuel;
    }
    public void setCapFuel(int capFuel){
        this.capFuel = capFuel;
    }
    public double getConFuel(){
        return this.conFuel;
    }
    public void setConFuel(double conFuel){
        this.conFuel = conFuel;
    }


}
